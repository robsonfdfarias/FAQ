                        <p></p>



                        <p>1) <strong>A ENTIDADE NÃO É <strong> RECONHECIDA DE UTILIDADE PÚBLICA COMO POSSO SOLICITAR O RECONHECIMENTO</strong>?</strong></p>



                        <p>R.: As entidades para serem reconhecidas de utilidade pública precisam estar de acordo com a <a href="http://leis.alesc.sc.gov.br/html/2021/18269_2021_lei.html" target="_blank" rel="noreferrer noopener">Lei nº 18.269, de 9 de dezembro de 2021</a>.</p>



                        <p>Os documentos originais estabelecidos no art. 3º da Lei nº 18.269, de 2021, serão encaminhados para o deputado, membro do parlamento catarinense, por meio de requerimento, obrigatório, conforme modelo <a href="https://portalsei.alesc.sc.gov.br/wp-content/uploads/2021/12/Solicitacao-de-utilidade-publica.docx" target="_blank" rel="noreferrer noopener">aqui</a>.</p>



                        <p>Lembre-se que são necessários todos os documentos comprobatórios estabelecidos no art. 3º, da <a href="http://leis.alesc.sc.gov.br/html/2021/18269_2021_lei.html" target="_blank" rel="noreferrer noopener">Lei nº 18.269, de 2021</a> para que sua entidade ser reconhecida de utilidade pública! </p>



                        <p>Os documentos físicos serão encaminhados para o deputado, no endereço:</p>



                        <p><strong>Assembleia Legislativa do Estado de Santa Catarina – Palácio Barriga Verde – Rua Doutor Jorge Luz Fontes, 310, CEP: 88020-900 – Florianópolis – Santa Catarina – Fone: (48) 3221-2500, <em><u><span class="has-inline-color has-vivid-red-color">ATENÇÃO: escreva aqui para qual o gabinete parlamentar está encaminhando.</span></u></em></strong></p>



                        <p><strong>OBS</strong>.: Após a publicação da lei que reconhece a utilidade pública a entidade poderá retirar todos os documentos físicos no gabinete que deu início ao processo.</p>



                        <p>2) <strong><strong>COMO PROCEDER COM A MANUTENÇÃO DO TÍTULO DE UTILIDADE PÚBLICA? </strong></strong></p>



                        <p>R.: O primeiro passo para realizar a manutenção do título é realizar o cadastro de usuário externo, veja o manual <a href="https://portalsei.alesc.sc.gov.br/perguntas-frequentes/passo-a-passo-para-cadastro-de-usuario-externo-no-sei/">aqui</a> e o vídeo <a href="https://portalsei.alesc.sc.gov.br/perguntas-frequentes/como-cadastrar-usuario-externo-no-sei/">aqui</a>.</p>



                        <p>A prestação de contas para entidades já reconhecidas de utilidade pública para a manutenção do Título de Utilidade Pública, é estabelecida por meio do <a href="http://leis.alesc.sc.gov.br/html/2021/18269_2021_lei.html">art. 7º da Lei nº 18.269, de 2021</a>, que dispõe sobre a concessão e a manutenção do Título de Utilidade Pública estadual no âmbito do Estado de Santa Catarina. Você pode ver o passo a passo para prestar contas <a href="https://drive.google.com/file/d/1BVWGxKoXboZ76qbZ7O7DcC8gkruH4_wJ/view?usp=sharing" target="_blank" rel="noreferrer noopener">clicando aqui</a>.</p>



                        <p></p>



                        <p>3) <strong>QUAIS DOCUMENTOS SÃO NECESSÁRIOS PARA PRESTAR CONTAS?</strong></p>



                        <p>R.: Os documentos descritos no art. 7º da <a rel="noreferrer noopener" href="http://leis.alesc.sc.gov.br/html/2021/18269_2021_lei.html" target="_blank">Lei nº 18.269, de 2021</a>, que devem ser encaminhados por meio do e-mail <a href="mailto:protocologeral@alesc.sc.gov.br">protocologeral@alesc.sc.gov.br</a>, obrigatoriamente, para que seja criado o processo e dê o encaminhamento para análise e emissão da certidão de manutenção do Título de Utilidade Pública, são os seguintes:</p>



                        <p>1) requerimento solicitando a manutenção, <a rel="noreferrer noopener" href="https://portalsei.alesc.sc.gov.br/wp-content/uploads/2021/12/18269_2021_Lei_Requerimento.docx" target="_blank">anexo único</a> da <a href="http://leis.alesc.sc.gov.br/html/2021/18269_2021_lei.html" target="_blank" rel="noreferrer noopener">Lei nº 18.269, de 2021</a>;</p>



                        <p>2) relatório das atividades do exercício anterior;</p>



                        <p>3) atestado de funcionamento atualizado;</p>



                        <p>4) certidão atualizada do registro da entidade no Cartório de Registro de Pessoas Jurídicas;</p>



                        <p>5) declaração do Presidente da entidade atestando o recebimento, ou não, de verba pública, no exercício anterior à solicitação e, em caso afirmativo, especificando o valor, a origem e a destinação; e</p>



                        <p>6) declaração do Presidente da entidade atestando a não qualificação como Organização da Sociedade Civil de Interesse Público (OSCIP), nos termos do art. 18 da Lei federal nº 9.790, de 1999, conforme modelo <a rel="noreferrer noopener" href="https://portalsei.alesc.sc.gov.br/wp-content/uploads/2021/12/Declaracao-nao-OSCIPv3.docx" target="_blank">aqui</a>.</p>



                        <p><span class="has-inline-color has-vivid-red-color"><strong>Lembre-se que todos os documentos são obrigatórios e devem ser preenchidos corretamente</strong>!</span></p>



                        <p>4) <strong><strong>QUAL FORMATO DO DOCUMENTO É NECESSÁRIO PARA A PRESTAÇÃO DE CONTAS DE ENTIDADES JÁ RECONHECIDAS DE UTILIDADE PÚBLIC</strong>A?</strong></p>



                        <p>R.: Os documentos devem ser salvos ou digitalizados a partir do documento original, obrigatoriamente, em arquivos: “.pdf”, “.pdf/A”, de preferência! Você poderá ver a <a rel="noreferrer noopener" href="http://leis.alesc.sc.gov.br/html/instrucao_normativa/2021/001_2021.html" target="_blank">Instrução Normativa nº 001, de 28 de maio de 2021</a><strong><em> </em></strong>, que estabelece os procedimentos para digitalização, se for do seu interesse!</p>



                        <p><strong>OBS</strong>.: Os documentos físicos devem ser guardados pelas entidades para casos de conferência!</p>



                        <p>5) <strong>COMO POSSO CONSULTAR MEU PROCESSO DE UTILIDADE PÚBLICA?</strong></p>



                        <p>R.: Assim que o documento for protocolado a pessoa autorizada a receber a disponibilização do acesso externo receberá um e-mail com o link para a consulta do andamento do processo.</p>



                        <p>O usuário externo cadastrado receberá a disponibilização do acesso ao processo por meio do e-mail, além do acesso por meio do login e senha. </p>



                        <p><strong>6) COMO FAÇO PARA SOLICITAR ALTERAÇÃO DA SEDE OU DA DENOMINAÇÃO SOCIAL DA ENTIDADE?</strong></p>



                        <p>R.: Para solicitar a alteração da sede ou da denominação social da entidade deve-se observar o disposto no art. 5º da Lei nº 18.269, de 2021, sendo necessário preencher o requerimento (<a href="https://portalsei.alesc.sc.gov.br/wp-content/uploads/2022/07/Alteracao-sede-ou-denominacao-utilidade-publica.docx" data-type="URL" data-id="https://portalsei.alesc.sc.gov.br/wp-content/uploads/2022/07/Alteracao-sede-ou-denominacao-utilidade-publica.docx" target="_blank" rel="noreferrer noopener">clique aqui</a>) e encaminhar para o e-mail protocologeral@alesc.sc.gov.br com os documentos comprobatórios.</p>



                        <p><strong>7) COMO SOLICITAR A REVOGAÇÃO DA LEI OU DO ATO DA MESA QUE OUTORGOU A DECLARAÇÃO DE ENTIDADE PÚBLICA?</strong></p>



                        <p>R.: Para solicitar a revogação da lei deve-se observar o disposto no art. 6ºda Lei nº 18.269, de 2021, sendo necessário preencher o requerimento (<a href="https://portalsei.alesc.sc.gov.br/wp-content/uploads/2021/12/Revogacao-de-utilidade-publica.docx">clique aqui</a>) e encaminhar para o e-mail protocologeral@alesc.sc.gov.br com os documentos comprobatórios.</p>



                        <p><strong>8) COMO FAÇO PARA SABER QUAL A LEI OU O ATO DA MINHA ENTIDADE?</strong></p>



                        <p>R.: Para saber qual a lei ou o ato da mesa que outorgou a declaração de entidade pública deve-se realizar a pesquisa em <a href="http://leis.alesc.sc.gov.br/">http://leis.alesc.sc.gov.br/</a></p>



                        <p>A pesquisa deve ser realizada pelo nome da entidade.</p>



                        <p>A <a rel="noreferrer noopener" href="http://leis.alesc.sc.gov.br/html/2021/18278_2021_lei.html" target="_blank">Lei nº 18.278, de 20 de dezembro de 2021</a> contém as leis consolidadas anteriores a 2015.</p>



                        <p><strong>9) COMO FAÇO PARA VERIFICAR A SITUAÇÃO DA ENTIDADE PÚBLICA?</strong><br></p>



                        <p>A situação da Entidade Pública pode ser verificada no site da Alesc, clicando <a href="https://www.alesc.sc.gov.br/entidades-utilidade-publica" target="_blank" rel="noreferrer noopener">aqui</a>!<br>A pesquisa pode ser realizada por meio do nome da entidade e por município.<br>Neste espaço é possível ter acesso às informações públicas da Entidade e obter a certidão vigente.</p>



                        <p>Em caso de dúvidas contato com a Coordenadoria de Documentação, por telefone: (48) 3221-2876 ou no e-mail: <a rel="noreferrer noopener" href="mailto:utilidadepublica@alesc.sc.gov.br" target="_blank">utilidadepublica@alesc.sc.gov.br</a>.</p>


